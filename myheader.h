void add(int a,int b){
	int sum = 0;
	sum = a + b;
	printf("Sum is %d\n",sum);
}

void subtract(int a,int b){
	int difference = 0;
	difference = a - b;
	printf("%d\n",difference);
}

void multiply(int a,int b){
	int product = 1;
	product = a * b;
	printf("%d",product);
}

void division(int a,int b){
	float result = 0;
	result = (float)a / b;
	printf("%d",result);
}